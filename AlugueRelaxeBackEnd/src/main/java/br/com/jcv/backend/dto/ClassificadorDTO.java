package br.com.jcv.backend.dto;

import java.io.Serializable;

public class ClassificadorDTO implements Serializable {
	private static final long serialVersionUID = -1393420832991018188L;
	public int notaClassificador;
	public String titulo;
	public String comentarioClassificador;
	
	public ClassificadorDTO(int notaClassificador, String titulo, String comentarioClassificador) {
		this.notaClassificador = notaClassificador;
		this.titulo = titulo;
		this.comentarioClassificador = comentarioClassificador;
	}
}