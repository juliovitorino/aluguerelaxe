package br.com.jcv.aluguerelaxe.client.componente.listbox;

public class StatusClienteListBox extends ComponenteListBox {
	
	public StatusClienteListBox() {
		super();
		this.preencheTipoAnunciante();
	}
	
	public StatusClienteListBox(boolean b) {
		super(b);
		this.preencheTipoAnunciante();
	}

	
	private void preencheTipoAnunciante() {
		this.addItem("Ativo","A");                 
		this.addItem("Inativo","I");                 
		this.addItem("Bloqueado","B");              
		this.addItem("Pendente","P");              
		this.addItem("Verificar","V");              
		this.addItem("Reprovado","R");              
	}

}
